package Java_Hackathon;

public class Creating_string_differentWay {

	public static void main(String[] args) {
		
		System.out.println();
        
		String str1="First way of creating string object by Literal way.";
		
		System.out.println(str1);
		
		System.out.println("--------------------------------------------------------------------------------------------------");

		System.out.println();

		String str=new String("Another way of creating string object by uisng new keyword.");
		
		System.out.println(str);
		
		System.out.println("--------------------------------------------------------------------------------------------------");

		
	}

}
